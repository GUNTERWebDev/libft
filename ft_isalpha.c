#include "libft.h"

int	ft_isalpha(int c)
{
	char	k;

	k = (char)c;
	if ((k >= 'a' && k <= 'z') || (k >= 'A' && k <= 'Z'))
	{
		return (1);
	}
	else
		return (0);
}
